    @Override
    public void run() {

        while (true) {
            if (boardManager.SEQUENCE) {
                timerThread = null;
                victory gane = new victory(boardManager.WINNER, boardManager.teamWin);
                gane.setVisible(true);
                this.dispose();
                break;
            }

            if (timesUp) {
                this.timerThread = null;
                break;
            }
            try {
                if (sec == 0) {
                    if (min == 0) {

                        min = 0;
                        sec = 59;
                        turnarEntreEquipos();
                        turnarInternamente();
                        Mazo.setManojo(this.leftManojo, this.rightManojo, this.BottomManojo, enTurno);

                    } else {
                        min--;
                        sec = 59;
                    }
                } else {

                    sec--;
                }

                if (sec < 10) {
                    this.timerCont.setText(("0" + min + ":0" + sec));
                } else {
                    this.timerCont.setText(("0" + min + ":" + sec));
                }

                Thread.sleep(1000);
            } catch (InterruptedException ex) {
                ex.printStackTrace();
            }

        }

    }


