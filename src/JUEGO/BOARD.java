    public static void turnarInternamente() {
        switch (equipoTurn) {
            case 1:
                if (cont == MenuPrincipal.team1.size()) {
                    cont = 0;

                }
                enTurno = MenuPrincipal.team1.get(cont);
                cont++;

                turnador.setText("TURNO DE " + enTurno.getUsername());
                break;
            case 2:
                if (cont == MenuPrincipal.team2.size()) {
                    cont = 0;

                }
                enTurno = MenuPrincipal.team2.get(cont);
                turnador.setText("TURNO DE " + enTurno.getUsername());
                break;
            case 3:
                if (cont == MenuPrincipal.team3.size()) {
                    cont = 0;

                }
                enTurno = MenuPrincipal.team3.get(cont);
                turnador.setText("TURNO DE " + enTurno.getUsername());
                break;

        }
    }

    public static void turnarEntreEquipos() {
        int cant = MenuPrincipal.cantEquipos;

        if (cant == 2 && equipoTurn == 2) {
            equipoTurn = 1;
            return;
        }

        if (cant == 3 && equipoTurn == 3) {
            equipoTurn = 1;
            return;
        }

        equipoTurn++;
    }

    private void showPlayerCards(JPanel team, ArrayList<Usuario> players) {
        ArrayList<JPanel> panels = this.stripPanels(team);
        for (int i = 0; i < players.size(); i++) {
            ArrayList<JLabel> currentPanelLabels = this.stripLabels(panels.get(i));
            this.setInfo(currentPanelLabels.get(0), players.get(i));
            
        }
    }

    private ArrayList<JLabel> stripLabels(JPanel panel) {
        ArrayList<JLabel> labels = new ArrayList();
        for (Component label : panel.getComponents()) {
            if (label instanceof JLabel) {
                labels.add((JLabel) label);
            }
        }
        return labels;
    }

    private ArrayList<JPanel> stripPanels(JPanel panel) {
        ArrayList<JPanel> panels = new ArrayList();
        for (Component panelito : panel.getComponents()) {
            if (panelito instanceof JPanel) {
                panels.add((JPanel) panelito);
            }
        }
        return panels;
    }

    private void setInfo(JLabel label, Usuario player) {

        if (label.getText().equals("NAME")) {
            label.setText(player.getUsername());
        }
       
    }
