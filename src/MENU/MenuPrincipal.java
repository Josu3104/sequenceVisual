    public boolean areTeamsEven() {

        if (p1.isEmpty() && p2.isEmpty() && p3.isEmpty()) {
            JOptionPane.showMessageDialog(null, "LLENE LOS EQUIPOS");
            return false;
        }

        switch (cantPlayersSettings) {
            case 2:
                return p1.size() == p2.size() && (p1.size() + p2.size()) == 2;
            case 3:
                return (p1.size() == p2.size() && p2.size() == p3.size()) && (p1.size() + p2.size() + p3.size()) == 3;
            case 4:
                return p1.size() == p2.size() && (p1.size() + p2.size()) == 4;
            case 6:
                return (p1.size() == p2.size() && p2.size() == p3.size()) && (p1.size() + p2.size() + p3.size()) == 6;
            case 8:
                return p1.size() == p2.size() && (p1.size() + p2.size()) == 8;
            default:
                return false;
        }
    }
