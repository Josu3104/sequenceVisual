    private void initReports(String userEnSesion) throws IOException, ClassNotFoundException {
        Usuario Logged = ManagerUsuarios.SearchUser(userEnSesion);
        this.playerDispReports.setText("JUGADOR EN SESION: " + userEnSesion);
        this.dateCreatedReports.setText("SE UNIO EN: " + fechaNeitor.format(Logged.getFechaCreacion()));
        if (Logged.getHistorial().isEmpty()) {
            this.historialDisplay.setText("AUN NO SE HAN REGISTRADO JUEGOS");
        } else {
            print(Logged, this.historialDisplay);
        }

    }

    private void print(Usuario logged, JTextArea text) {
        StringBuilder builder = new StringBuilder();
        for (historial report : logged.getHistorial()) {
            builder.append(report.toString()).append("\n");
        }
        text.setText(builder.toString());
    }

