    private void insertPlayer(JList players, ArrayList<String> array) {
        DefaultListModel<String> mainList = new DefaultListModel<>();
        DefaultListModel<String> miniList = new DefaultListModel<>();

        array.add(usersList.getSelectedValue());
        for (String a : array) {
            miniList.addElement(a);
        }

        PLAYERS.remove(this.usersList.getSelectedValue());

        for (String a : PLAYERS) {
            mainList.addElement(a);
        }

        usersList.setModel(mainList);
        players.setModel(miniList);
        usersList.clearSelection();

    }

    private void removePlayer(JList<String> players, ArrayList<String> array) {
        DefaultListModel<String> mainList = new DefaultListModel<>();
        DefaultListModel<String> miniList = new DefaultListModel<>();

        array.remove(players.getSelectedValue());
        for (String a : array) {
            miniList.addElement(a);
        }

        PLAYERS.add(players.getSelectedValue());
        for (String a : PLAYERS) {
            mainList.addElement(a);
        }

        usersList.setModel(mainList);
        players.setModel(miniList);
        players.clearSelection();

    }

